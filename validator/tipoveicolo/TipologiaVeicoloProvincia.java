	/**
	 * Checks if is provincia.
	 *
	 * @param value the value
	 * @return true, if is provincia
	 */
	static boolean isProvincia(String value) {
		boolean ret = false;
		if (listaProvincia.containsKey(value.toUpperCase())) {
			ret = true;
		}
		return ret;
	}

	/**
	 * Checks if is barletta trani.
	 *
	 * @param value the value
	 * @return true, if is barletta trani
	 */
	static boolean isBarlettaTrani(String value) {
		boolean ret = false;
		if (value.equalsIgnoreCase(SIGLA_BARLETTA_TRANI)) {
			ret = true;
		}
		return ret;
	}

	/**
	 * Checks if is agrigento.
	 *
	 * @param value the value
	 * @return true, if is agrigento
	 */
	static boolean isAgrigento(String value) {
		boolean ret = false;
		if (value.equalsIgnoreCase(SIGLA_AGRIGENTO)) {
			ret = true;
		}
		return ret;
	}

	/**
	 * Checks if is provincia AMRSTV.
	 *
	 * @param value the value
	 * @return true, if is provincia AMRSTV
	 */
	static boolean isProvinciaAMRSTV(String value) {
		boolean ret = true;
		if (value.equalsIgnoreCase(SIGLA_FORLI_CESENA) || value.equalsIgnoreCase(SIGLA_PESARO_URBINO) || value.equalsIgnoreCase(SIGLA_MEDIO_CAMPIDANO) || value.equalsIgnoreCase(SIGLA_BARLETTA_TRANI) || value.equalsIgnoreCase(SIGLA_FERMO) || value.equalsIgnoreCase(SIGLA_MONZA_BRIANZA) || value.equalsIgnoreCase(SIGLA_OGLIASTRA) || value.equalsIgnoreCase(SIGLA_OLBIA_TEMPIO) || value.equalsIgnoreCase(SIGLA_CARBONIA_IGLESIAS)) {
			ret = false;
		}
		return ret;
	}

